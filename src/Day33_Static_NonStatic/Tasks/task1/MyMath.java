package Day33_Static_NonStatic.Tasks.task1;

public class MyMath {
    public static int minsayi(int value1,int value2){

        return Math.min(value1,value2);
    }

    public static int maxsayi(int value1,int value2){

        return Math.max(value1,value2);
    }

    public static int mutlak(int value1){

        return Math.abs(value1);
    }

    public static double usalma(int value1,int value2){

        return Math.pow(value1,value2);
    }

    public static double karekok(int value1){

        return Math.sqrt(value1);
    }
}
