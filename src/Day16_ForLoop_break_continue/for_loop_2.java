package Day16_ForLoop_break_continue;

public class for_loop_2 {
    public static void main(String[] args) {
        //1 den 10 a kadar olan sayilari yazdiriniz...
        // 10 dan 0 a kadar olan sayilari yazdirin...

        for (int i=0; i<=10;i++) {
            System.out.println("forward= " +i);
        }

        for(int i=10; i>=0; i--) {
            System.out.println("back_co= " +i);
        }
    }

}
