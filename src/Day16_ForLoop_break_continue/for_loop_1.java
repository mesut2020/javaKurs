package Day16_ForLoop_break_continue;

public class for_loop_1 {
    public static void main(String[] args) {
        // While loop

        int i=0;
        while (i<5) {
            System.out.println("w_i=" + i);
            i++;
        }

        for(i=0;i<5;i++) {
            System.out.println("f_i=" + i);
        }

    }
}
