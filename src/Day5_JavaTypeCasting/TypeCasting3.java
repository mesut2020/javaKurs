package Day5_JavaTypeCasting;

public class TypeCasting3 {
    public static void main(String[] args) {
        // short ve byte cinsinden iki degisken e deger atayin...
        // ve short u byte turune cevirin...

        short num1 = 200;
        short num2 = 128;

        byte num3 = (byte)num1;
        byte num4 = (byte)num2;

        System.out.println("num3 = " + num3);
        System.out.println("num4 = " + num4);




    }
}
