package Day17_NestedLoops;

public class NestedLoop2 {
    public static void main(String[] args) {
        //Asagidaki sekilde cikti veren programi print icerisinde sadece
        // bir tane # kullanarak 5x5 kare yapin ici dolu...



        for(int i=1;i<=5;i++)
        {
            for (int j=1; j<=5; j++)
            {
                System.out.print("#");
            }
            System.out.println();
        }



    }
}
