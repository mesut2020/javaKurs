package Day3_Comment_Print_DataTypes;

public class DataTypes {
    public static void main(String[] args) {
        byte byteValue = 4;
        short shortValue = 3000;
        int intValue = 200000;
        long longValue = 123456789;

        float floatValue = 3.14f;
        double doubleValue = 3.14;

        char charValue1 = 'A';
        char charValue2 = 65;  // Hexadecimal kodunu karaktere cevirir...

        boolean boolValue1 = true;
        boolean boolValue2 = false;

        System.out.println("byteDeger = " + byteValue);
        System.out.println("shortDeger = " + shortValue);
        System.out.println("intDeger = " + intValue);
        System.out.println("longDeger = " + longValue);
        System.out.println("floatDeger = " + floatValue);
        System.out.println("doubleDeger = " + doubleValue);
        System.out.println("charDeger1 = " + charValue1);
        System.out.println("charDeger2 = " + charValue2);
        System.out.println("boolDeger1 = " + boolValue1);
        System.out.println("boolDeger2 = " + boolValue2);

    }
}